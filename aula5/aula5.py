# Operadores Aritiméticos

'''
+, -, *, /, //, **, %, ()
'''

print(10 // 3)  # Divisão  arrendondar para inteira
print(2 ** 10)  # Potenciação
print(10 % 5)  # Resto da divisão
print(5 + 2 * 10)  # Alterar a precedência dos operadores
print((5 + 2) * 10)  # Alterar a precedência dos operadores
print(20 * 'A')
print(20 * 2)
print('20' + 'A')
print(20 + 2)
print(2 + 5 * 3 ** 2 - (23.5 + 23.5))
